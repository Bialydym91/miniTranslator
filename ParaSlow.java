package university.translator2;

public class ParaSlow {
    private String angielskie;
    private String polskie;

    public ParaSlow(String angielskie, String polskie) {
        this.angielskie = angielskie;
        this.polskie = polskie;
    }

    public String getAngielskie() {
        return angielskie;
    }

    public String getPolskie() {
        return polskie;
    }
}
